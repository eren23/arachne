#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn atlas_dimensions_correct() {
        let (data, font) = generate_builtin_font();
        assert_eq!(data.len(), (ATLAS_W * ATLAS_H * 4) as usize);
        assert_eq!(font.glyphs.len(), 96); // ASCII 32..=127
    }

    #[test]
    fn all_printable_ascii_have_glyphs() {
        let (_, font) = generate_builtin_font();
        for code in 32u32..=127 {
            assert!(
                font.glyphs.contains_key(&code),
                "missing glyph for char {}",
                code,
            );
        }
    }

    #[test]
    fn glyph_metrics_within_atlas() {
        let (_, font) = generate_builtin_font();
        for glyph in font.glyphs.values() {
            assert!(glyph.x + glyph.width <= ATLAS_W as f32, "glyph {} overflows atlas x", glyph.id);
            assert!(glyph.y + glyph.height <= ATLAS_H as f32, "glyph {} overflows atlas y", glyph.id);
        }
    }

    #[test]
    fn letter_a_has_nonzero_pixels() {
        let (data, font) = generate_builtin_font();
        let glyph = font.glyphs.get(&(b'A' as u32)).unwrap();
        let mut has_pixel = false;
        for py in 0..glyph.height as u32 {
            for px in 0..glyph.width as u32 {
                let ax = glyph.x as u32 + px;
                let ay = glyph.y as u32 + py;
                let offset = ((ay * ATLAS_W + ax) * 4) as usize;
                if data[offset + 3] > 0 {
                    has_pixel = true;
                }
            }
        }
        assert!(has_pixel, "Letter 'A' should have at least one opaque pixel");
    }

    #[test]
    fn space_has_no_pixels() {
        let (data, font) = generate_builtin_font();
        let glyph = font.glyphs.get(&(b' ' as u32)).unwrap();
        for py in 0..glyph.height as u32 {
            for px in 0..glyph.width as u32 {
                let ax = glyph.x as u32 + px;
                let ay = glyph.y as u32 + py;
                let offset = ((ay * ATLAS_W + ax) * 4) as usize;
                assert_eq!(data[offset + 3], 0, "Space should have no opaque pixels");
            }
        }
    }

    #[test]
    fn layout_hello_arachne() {
        let (_, font) = generate_builtin_font();
        let quads = font.layout_text("Hello Arachne!", 8.0, None);
        // "Hello Arachne!" = 14 chars; all produce quads (space is a zero-size quad)
        assert_eq!(quads.len(), 14, "All 14 chars (including space) produce quads");
    }
}
