//! Built-in bitmap font for text rendering without external font files.
//!
//! Generates a minimal 5x7 pixel font atlas covering ASCII 32-127 and a
//! corresponding [`BmFont`] with glyph metrics. The atlas is returned as
//! RGBA8 pixel data ready for GPU upload.

use crate::render2d::text::BmFont;
use std::collections::HashMap;

/// Width of each glyph cell in the atlas (pixels).
pub const GLYPH_W: u32 = 6; // 5 pixels + 1 padding
/// Height of each glyph cell in the atlas (pixels).
pub const GLYPH_H: u32 = 8; // 7 pixels + 1 padding

/// Number of ASCII characters in the atlas (32..=127).
const CHAR_COUNT: u32 = 96;
/// Number of glyphs per row in the atlas.
const COLS: u32 = 16;
/// Number of rows in the atlas.
const ROWS: u32 = (CHAR_COUNT + COLS - 1) / COLS; // 6

/// Atlas width in pixels.
pub const ATLAS_W: u32 = COLS * GLYPH_W; // 96
/// Atlas height in pixels.
pub const ATLAS_H: u32 = ROWS * GLYPH_H; // 48

/// Generate the built-in font atlas as RGBA8 pixel data and a matching `BmFont`.
///
/// Returns `(rgba_data, font)` where `rgba_data` is `ATLAS_W * ATLAS_H * 4` bytes.
pub fn generate_builtin_font() -> (Vec<u8>, BmFont) {
    let mut pixels = vec![0u8; (ATLAS_W * ATLAS_H * 4) as usize];
    let mut glyphs = HashMap::new();

    for code in 32u32..=127 {
        let idx = code - 32;
        let col = idx % COLS;
        let row = idx / COLS;
        let origin_x = col * GLYPH_W;
        let origin_y = row * GLYPH_H;

        // Get the 5x7 bitmap for this character.
        let bitmap = glyph_bitmap(code as u8);

        // Write pixels into the atlas.
        for py in 0..7u32 {
            for px in 0..5u32 {
                let bit = (bitmap[py as usize] >> (4 - px)) & 1;
                if bit != 0 {
                    let ax = origin_x + px;
                    let ay = origin_y + py;
                    let offset = ((ay * ATLAS_W + ax) * 4) as usize;
                    pixels[offset] = 255;     // R
                    pixels[offset + 1] = 255; // G
                    pixels[offset + 2] = 255; // B
                    pixels[offset + 3] = 255; // A
                }
            }
        }

        // Register glyph metrics.
        // Space (and other whitespace) gets zero width/height so no quad is
        // generated, but still advances the cursor via xadvance.
        let (gw, gh) = if code == 32 { (0.0, 0.0) } else { (5.0, 7.0) };
        let glyph = crate::render2d::text::GlyphMetrics {
            id: code,
            x: origin_x as f32,
            y: origin_y as f32,
            width: gw,
            height: gh,
            xoffset: 0.0,
            yoffset: 0.0,
            xadvance: 6.0,
            page: 0,
        };
        glyphs.insert(code, glyph);
    }

    let font = BmFont {
        glyphs,
        kerning: HashMap::new(),
        line_height: 8.0,
        base: 7.0,
        scale_w: ATLAS_W as f32,
        scale_h: ATLAS_H as f32,
    };

    (pixels, font)
}

/// Return a 5x7 bitmap for the given ASCII byte.
